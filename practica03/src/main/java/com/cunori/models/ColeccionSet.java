public class ColeccionSet {
    /**
     * La interfaz Set define una colección que no puede contener elementos
     * duplicados. Esta interfaz contiene, únicamente, los métodos heredados de
     * Collection añadiendo la restricción de que los elementos duplicados están
     * prohibidos.
     */

    /**
     * HashSet: este implementación almacena los elementos en una tabla hash. Es
     * la implementación con mejor rendimiento de todas pero no garantiza ningún
     * orden a la hora de realizar iteraciones. Es la implementación más
     * empleada debido a su rendimiento y a que, generalmente, no nos importa el
     * orden que ocupen los elementos.
     */
    /**
     * TreeSet: esta implementación almacena los elementos ordenándolos en
     * función de sus valores. Es bastante más lento que HashSet. Los elementos
     * almacenados deben implementar la interfaz Comparable.
     */
    /**
     * LinkedHashSet: esta implementación almacena los elementos en función del
     * orden de inserción. Es, simplemente, un poco más costosa que HashSet.
     */


}
